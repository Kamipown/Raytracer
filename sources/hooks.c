#include "rtv1.h"

int		expose_hook(t_env *e)
{
	draw_image(e);
	return (0);
}

int		key_hook(int k, t_env *e)
{
	if (k == ESCAPE)
		exit(0);
	expose_hook(e);
	return (0);
}
